class CashUnitRow {
  String? unitName = "";
  String count = "";
  String amount = "";

  String? getUnitName() {
    return unitName;
  }

  void setUnitName(String? unitName) {
    this.unitName = unitName;
  }

  String getCount() {
    return count;
  }

  void setCount(String count) {
    this.count = count;
  }

  String getAmount() {
    return amount;
  }

  void setAmount(String amount) {
    this.amount = amount;
  }
}
